	SELECT 
		Аптеки.ID_Аптеки,
		Аптеки.Адрес_Аптеки,
		Лекарственные_препараты.ID_Препарата,
		Лекарственные_препараты.Название AS НазваниеПрепарата,
		Аптеки_Препараты.Количество
	FROM 
		Аптеки
	JOIN 
		Аптеки_Препараты ON Аптеки.ID_Аптеки = Аптеки_Препараты.ID_Aптеки
	JOIN 
		Лекарственные_препараты ON Аптеки_Препараты.ID_Препарата = Лекарственные_препараты.ID_Препарата;